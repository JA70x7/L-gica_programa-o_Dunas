# Adiçao
numero1 = 10
numero2 = 20

soma = numero1 + numero2
print(soma)

# Subtraçao

subtraçao = numero1 - numero2
print(subtraçao)

# Multiplicaçao

multiplicaçao = numero1 * numero2
print(multiplicaçao)

# Divisao

divisao = numero1 / numero2
print(divisao)

# Potencia

potencia = numero1 ** numero2
print(potencia)

# Modulo

modulo = numero1 % numero2
print(modulo)

# Resto da divisao

divisao = numero1 // numero2
print(divisao)
